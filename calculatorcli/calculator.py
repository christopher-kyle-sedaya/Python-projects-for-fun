class Calculator():
    def __init__(self):
        self.introduction_msg = "Welcome to the CalculatorCli!"

    def add(self, num1 ,num2):
        return num1 + num2

    def subtract(self, num1, num2):
        return num1 - num2

    def multiply(self, num1, num2):
        return num1 * num2

    def divide(self, num1, num2):
        return num1 / num2
